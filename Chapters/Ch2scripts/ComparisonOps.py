# COmparison operators in python

# Output True
print('42 = 42 is', 42 == 42)
# Output False
print('42 == 99 is', 42 == 99)
# Output True
print(42 != 99)
# Output False
print(2!=2)

# Output False
print(2=='2')
# Output True
print(2!='2')

